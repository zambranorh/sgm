package com.conecel.claro.lotconector.util;



public class LotResponse {
	
	private String customerId;
	private String activationDate;
	private String loggedUser;
	private String paymentType;
	private String financialInstitution;
	private String officceId;
	private String officce;
	private String days;
	private String status;
	private String reason;
	private String observation;
	private String regularizedUser;
	public String getCustomerId() {
		return customerId;
	}
	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}
	public String getActivationDate() {
		return activationDate;
	}
	public void setActivationDate(String activationDate) {
		this.activationDate = activationDate;
	}
	public String getLoggedUser() {
		return loggedUser;
	}
	public void setLoggedUser(String loggedUser) {
		this.loggedUser = loggedUser;
	}
	public String getPaymentType() {
		return paymentType;
	}
	public void setPaymentType(String paymentType) {
		this.paymentType = paymentType;
	}
	public String getFinancialInstitution() {
		return financialInstitution;
	}
	public void setFinancialInstitution(String financialInstitution) {
		this.financialInstitution = financialInstitution;
	}
	public String getOfficceId() {
		return officceId;
	}
	public void setOfficceId(String officceId) {
		this.officceId = officceId;
	}
	public String getOfficce() {
		return officce;
	}
	public void setOfficce(String officce) {
		this.officce = officce;
	}
	public String getDays() {
		return days;
	}
	public void setDays(String days) {
		this.days = days;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public String getObservation() {
		return observation;
	}
	public void setObservation(String observation) {
		this.observation = observation;
	}
	public String getRegularizedUser() {
		return regularizedUser;
	}
	public void setRegularizedUser(String regularizedUser) {
		this.regularizedUser = regularizedUser;
	}
	
	

}
